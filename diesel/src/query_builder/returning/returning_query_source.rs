//! The `ReturningQuerySource<Stmt, T>` wrapper used to type-check `RETURNING`
//! clauses, the statement-kind markers it is parameterized over, and the
//! [`InsertStmtKind`] dispatch trait.
//!
//! See [`ReturningQuerySource`] for the high-level picture.

use crate::query_source::joins::ToInnerJoin;
use crate::query_source::{AppearsInFromClause, Once};
use core::marker::PhantomData;

/// Statement-kind marker used as the `Stmt` parameter of
/// [`ReturningQuerySource`] for `INSERT` statements (other than
/// `INSERT ... ON CONFLICT ... DO UPDATE`, which uses
/// [`InsertOnConflictDoUpdateStmt`]).
///
/// This exists so that `RETURNING` typechecking can vary per statement kind
/// (relevant in PostgreSQL 18 and later, where `RETURNING old.col` is allowed
/// for some but not all statements). It is deliberately **separate** from the
/// SQL-keyword markers `Insert`/`InsertOrIgnore`/`Replace`, which control the
/// emitted keyword rather than the row-shape of `RETURNING`.
#[allow(missing_copy_implementations)]
#[derive(Debug)]
pub struct InsertStmt;

/// Statement-kind marker used as the `Stmt` parameter of
/// [`ReturningQuerySource`] for `UPDATE` statements.
///
/// Together with [`InsertOnConflictDoUpdateStmt`], this is one of the markers
/// for which [`crate::pg::returning::Old`] is `SelectableExpression` of a
/// [`ReturningQuerySource`], which is what makes `.returning(old(col))`
/// compile-time exclusive to `UPDATE` (and, with `.nullable()`,
/// `INSERT ... ON CONFLICT ... DO UPDATE`).
#[allow(missing_copy_implementations)]
#[derive(Debug)]
pub struct UpdateStmt;

/// Statement-kind marker used as the `Stmt` parameter of
/// [`ReturningQuerySource`] for `DELETE` statements.
#[allow(missing_copy_implementations)]
#[derive(Debug)]
pub struct DeleteStmt;

/// Statement-kind marker used as the `Stmt` parameter of
/// [`ReturningQuerySource`] for `INSERT ... ON CONFLICT ... DO UPDATE`
/// statements.
///
/// This is split out from [`InsertStmt`] because PostgreSQL 18+ accepts
/// `RETURNING old.col` here, but the value of `old.col` is `NULL` for rows
/// that were inserted (rather than updated). To keep that fact visible in the
/// type system, [`crate::pg::returning::Old`] does **not** implement
/// `SelectableExpression<ReturningQuerySource<InsertOnConflictDoUpdateStmt, _>>`
/// directly — only [`crate::expression::nullable::Nullable<Old<C>>`] does, via
/// the existing `Nullable` machinery and the [`ToInnerJoin`] mapping below
/// (which maps `InsertOnConflictDoUpdateStmt` to [`UpdateStmt`] for the
/// purposes of resolving the inner expression). This forces users to write
/// `old(col).nullable()` in this context, so deserialization is type-safe.
#[allow(missing_copy_implementations)]
#[derive(Debug)]
pub struct InsertOnConflictDoUpdateStmt;

/// Synthetic query source used as the `QS` parameter of
/// [`SelectableExpression`] / [`AppearsOnTable`] when type-checking
/// `RETURNING` clauses.
///
/// `Stmt` is one of [`UpdateStmt`], [`DeleteStmt`], [`InsertStmt`], or
/// [`InsertOnConflictDoUpdateStmt`]; `T` is the table that the
/// `INSERT`/`UPDATE`/`DELETE` is acting on.
///
/// Existing `RETURNING` users are not affected: every plain column
/// (`users::name`), every function application (`lower(name)`), every cast,
/// every `Selectable` struct, every tuple, etc. already has a
/// `SelectableExpression<QS>` impl that is generic in `QS` (or, for columns,
/// resolves through this module's `Column`-bound blanket and the
/// [`AppearsInFromClause`] forwarding impl below). The only place where the
/// `Stmt` parameter is observable is in [`crate::pg::returning::Old`], which
/// implements `SelectableExpression<ReturningQuerySource<UpdateStmt, _>>`
/// only — so `.returning(old(col))` is admitted only in `UPDATE` (and, with
/// `.nullable()`, in `INSERT ... ON CONFLICT ... DO UPDATE`), and rejected at
/// compile time in plain `INSERT` and `DELETE`.
#[allow(missing_copy_implementations)]
#[derive(Debug)]
pub struct ReturningQuerySource<Stmt, T>(PhantomData<(Stmt, T)>);

impl<Stmt, T> AppearsInFromClause<T> for ReturningQuerySource<Stmt, T> {
    type Count = Once;
}

// Per-column `SelectableExpression<ReturningQuerySource<Stmt, table>>` impls
// are generated by the `table!` macro (see `diesel_derives::table::expand_column`),
// alongside the existing per-column `SelectableExpression<Only<table>>` and
// `SelectableExpression<Tablesample<table, TSM>>` impls. We can't write a
// `<C: Column> SelectableExpression<ReturningQuerySource<Stmt, C::Table>> for C`
// blanket here because [`crate::expression::assume_not_null::AssumeNotNull<T>`]
// also implements `Column` and has its own generic-in-`QS`
// `SelectableExpression<QS>` impl, which would overlap at coherence.
//
// `AppearsOnTable<ReturningQuerySource<Stmt, table>>` for columns is provided
// by the existing generic
// `impl<QS> AppearsOnTable<QS> for column where QS: AppearsInFromClause<column::Table, Count=Once>`
// impl combined with the [`AppearsInFromClause`] forwarding impl above, so it
// does not need explicit per-column generation.

// Plain self-mapping for the simple cases.
impl<T> ToInnerJoin for ReturningQuerySource<UpdateStmt, T> {
    type InnerJoin = Self;
}

impl<T> ToInnerJoin for ReturningQuerySource<DeleteStmt, T> {
    type InnerJoin = Self;
}

impl<T> ToInnerJoin for ReturningQuerySource<InsertStmt, T> {
    type InnerJoin = Self;
}

// `InsertOnConflictDoUpdateStmt` maps to `UpdateStmt`. This is what makes
// `Nullable<Old<C>>: SelectableExpression<ReturningQuerySource<InsertOnConflictDoUpdateStmt, _>>`
// resolve through the existing
// `impl<T, QS> SelectableExpression<QS> for Nullable<T>
//     where QS: ToInnerJoin, T: SelectableExpression<QS::InnerJoin>` impl
// (see `crate::expression::nullable::Nullable`): that bound reduces to
// `Old<C>: SelectableExpression<ReturningQuerySource<UpdateStmt, _>>`, which
// holds, while the bare `Old<C>: SelectableExpression<ReturningQuerySource<InsertOnConflictDoUpdateStmt, _>>`
// is intentionally *not* implemented — forcing users to write
// `old(col).nullable()` in `ON CONFLICT ... DO UPDATE` for type safety.
impl<T> ToInnerJoin for ReturningQuerySource<InsertOnConflictDoUpdateStmt, T> {
    type InnerJoin = ReturningQuerySource<UpdateStmt, T>;
}

/// Maps an `InsertStatement` `Values` shape to the statement-kind marker that
/// should be used when type-checking that statement's `RETURNING` clause.
///
/// This is what makes `RETURNING old(col)` accept `INSERT ... ON CONFLICT
/// ... DO UPDATE` (where the marker is [`InsertOnConflictDoUpdateStmt`], for
/// which `Nullable<Old<C>>` is a valid `RETURNING` element) but reject plain
/// `INSERT` (where the marker is [`InsertStmt`], for which `Old<C>` does not
/// implement `SelectableExpression`).
///
/// The trait is sealed in spirit — it only has impls for the values shapes
/// `diesel` itself produces — but it is exposed publicly under the
/// `i-implement-a-third-party-backend-and-opt-into-breaking-changes` feature
/// so third-party backends that introduce new values shapes can add their own
/// impls.
pub trait InsertStmtKind {
    /// The statement-kind marker (see e.g. [`InsertStmt`],
    /// [`InsertOnConflictDoUpdateStmt`]) used as the `Stmt` parameter of
    /// [`ReturningQuerySource`] for `INSERT` statements with this `Values`
    /// shape.
    type StmtKind;
}

impl<T, Tab> InsertStmtKind for crate::query_builder::ValuesClause<T, Tab> {
    type StmtKind = InsertStmt;
}

impl<V, Tab, QId, const STABLE_QUERY_ID: bool> InsertStmtKind
    for crate::query_builder::BatchInsert<V, Tab, QId, STABLE_QUERY_ID>
{
    type StmtKind = InsertStmt;
}

impl InsertStmtKind for crate::query_builder::insert_statement::DefaultValues {
    type StmtKind = InsertStmt;
}

impl<S, C> InsertStmtKind for crate::query_builder::insert_statement::InsertFromSelect<S, C> {
    type StmtKind = InsertStmt;
}

impl<V, Target, T, WhereClause> InsertStmtKind
    for crate::query_builder::upsert::on_conflict_clause::OnConflictValues<
        V,
        Target,
        crate::query_builder::upsert::on_conflict_actions::DoNothing<T>,
        WhereClause,
    >
{
    // ON CONFLICT DO NOTHING does not return the lines that conflicted if using RETURNING
    type StmtKind = InsertStmt;
}

impl<V, Target, Changeset, Tab, WhereClause> InsertStmtKind
    for crate::query_builder::upsert::on_conflict_clause::OnConflictValues<
        V,
        Target,
        crate::query_builder::upsert::on_conflict_actions::DoUpdate<Changeset, Tab>,
        WhereClause,
    >
{
    type StmtKind = InsertOnConflictDoUpdateStmt;
}
